""" Operadores Aritméticos, Comparação e Atribuição """

valor_1 = 20
valor_2 = 10

# soma
print(valor_1 + valor_2)

# subtração
print(valor_1 - valor_2)

# divisão e divisão inteira
print(valor_1 / valor_2)
print(valor_1 // valor_2)

# multiplicação
print(valor_1 * valor_2)

# módulo
print(valor_1 % valor_2)

# exponenciação
print(valor_1 ** valor_2)

# ordem explícita com parênteses
print((10 / 2) + (25 * 2) - (2 ** 2))

""" Comparação """
saldo = 200
saque = 200

# igualdade
print(saldo == saque)

# diferença
print(saldo != saque)

# maior e maior ou igual
print(saldo > saque)
print(saldo >= saque)

# menor e menor ou igual
print(saldo < saque)
print(saldo <= saque)

